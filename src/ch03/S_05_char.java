package ch03;

public class S_05_char {
    public static void main(String[] args) {
        char a1 = 'a';       // 문자
        char a2 = 97;        // 아스키코드
        char a3 = '\u0061';  // 유니코드

        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);

        // 문잣값을 표현하는 방식은 다양함.
    }
}
